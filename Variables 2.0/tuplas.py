"""
La tupla optimisa el proseso de memoria cuando
nitamos datos de solo lectura xq es inmutable

creando tupla con tuple()
tuple recibe una lista -> ["", ""]
"""
tupla = tuple(["dato 1", "dato 1"])

#otra forma de crear tupla sin parentesis
tupla = "dato 1", "dato 1"

# tupla de un solo dato con la coma al final ahora es una tupla
tupla = "dato 1",

print(tupla)